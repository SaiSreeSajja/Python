x=12
y=2

bitwise_and = x & y
bitwise_or = x | y
bitwise_xor = x ^ y
bitwise_not_x = ~x
left_shift = x << 2
right_shift = y >> 2

print("Bitwise AND:", bitwise_and)
print("Bitwise OR:", bitwise_or)
print("Bitwise XOR:", bitwise_xor)
print("Bitwise NOT on x:", bitwise_not_x)
print("Left shift x by 2:", left_shift)
print("Right shift y by 2:", right_shift)

